// script.js
document.addEventListener("DOMContentLoaded", function () {
    // Mengambil referensi ke elemen container
    const container = document.querySelector('.Peraturan');
    
    // Membuat observer untuk memantau elemen saat berada di dalam viewport
    const observer = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          // Jika container terlihat di layar, tambahkan class 'visible' untuk animasi
          entry.target.classList.add('visible');
        } else {
          // Jika container tidak terlihat di layar, hapus class 'visible'
          entry.target.classList.remove('visible');
        }
      });
    }, {
      threshold: 0.5 // Atur ambang batas, 50% elemen harus terlihat untuk memicu
    });
  
    // Mulai memantau container
    observer.observe(container);
  });
  
